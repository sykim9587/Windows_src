/*
	다중for문
	형식
		for(초기식 1;  ;  ){
			for(초기식 2;  ;  ){}
		}
	초기식 1 은 한번만 
*/

#include <stdio.h>

int main() {
	/* 출력 형식 1
		#####
	*/
	for (int i = 1; i <= 5; i++) {
		printf("#");
	}
	
	printf("\n------------------------------------------------------------------------\n");
	/* 형식 2
		#####
		#####
		#####
		#####
		#####
	*/
	for (int k = 1; k <= 5; k++) {
		for (int i = 1; i <= 5; i++) {
			printf("#");
		}
		printf("\n");
	}

	printf("\n------------------------------------------------------------------------\n");

	/* 형식 3
	#
	##
	###
	####
	#####
	*/
	for (int k = 1; k <= 5; k++) {
		for (int i = 1; i <= k; i++) {
			printf("#");
		}
		printf("\n");
	}
	printf("\n------------------------------------------------------------------------\n");
	/* 형식 4
		#####
		####
		###
		##
		#
	*/
	for (int k = 5; k >= 1; k--) {
		for (int i = 1; i <= k; i++) {
			printf("#");
		}
		printf("\n");
	}
	return 0;
}